                $(".fiveSecond-mask").fadeIn();
                //        5秒关闭弹窗
                var s=$("#five-s").html();
                var timer=setInterval(function () {
                    s--;
                    $("#five-s").html(s);
                    if(s<1){
                        clearInterval(timer);
                        closeFiveSecond();
                        return false;
                    }
                },1000);
            },1000);
        }
